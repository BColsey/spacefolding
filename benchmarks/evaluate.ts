function computeMetrics(retrieved: string[], relevant: Set<string>, totalRelevant: number): Metrics {
  const recallAt = (k: number) => {
    const topK = retrieved.slice(0, k);
    const hits = topK.filter((p) => relevant.has(p)).length;
    return totalRelevant > 0 ? hits / totalRelevant : 0;
  };

  const precisionAt = (k: number) => {
    if (k === 0) return 0;
    const topK = retrieved.slice(0, k);
    return topK.filter((p) => relevant.has(p)).length / k;
  };

  const ndcgAt = (k: number) => {
    const topK = retrieved.slice(0, k);
    let dcg = 0;
    for (let i = 0; i < topK.length; i++) {
      const rel = relevant.has(topK[i]) ? 1 : 0;
      dcg += rel / Math.log2(i + 2); // i+2 because log2(1) = 0
    }
    let idcg = 0;
    const idealCount = Math.min(totalRelevant, k);
    for (let i = 0; i < idealCount; i++) {
      idcg += 1 / Math.log2(i + 2);
    }
    return idcg > 0 ? dcg / idcg : 0;
  };

  const mrr = (() => {
    for (let i = 0; i < retrieved.length; i++) {
      if (relevant.has(retrieved[i])) return 1 / (i + 1);
    }
    return 0;
  })();

  // hits@k: 1.0 if any relevant file appears in the top-k results, else 0.0.
  // These are the most meaningful metrics when a task has a single gold file.
  const hitsAt = (k: number) => (retrieved.slice(0, k).some((p) => relevant.has(p)) ? 1 : 0);

  return {
    recallAt5: recallAt(5),
    recallAt10: recallAt(10),
    recallAt20: recallAt(20),
    precisionAt5: precisionAt(5),
    precisionAt10: precisionAt(10),
    precisionAt20: precisionAt(20),
    ndcgAt10: ndcgAt(10),
    ndcgAt20: ndcgAt(20),
    mrr,
    hitsAt1: hitsAt(1),
    hitsAt5: hitsAt(5),
    avgResults: retrieved.length,
  };
}

function readOptionValue(argv: string[], index: number, flag: string): string {
  const value = argv[index + 1];
  if (!value || value.startsWith('--')) {
    throw new Error(`${flag} requires a value`);
  }
  return value;
}

function parsePositiveInteger(value: string, flag: string): number {
  if (!/^\d+$/.test(value)) {
    throw new Error(`${flag} must be a positive integer`);
  }
  const parsed = Number(value);
  if (!Number.isSafeInteger(parsed) || parsed < 1) {
    throw new Error(`${flag} must be a positive integer`);
  }
  return parsed;
}

function errorMessage(error: unknown): string {
  return error instanceof Error ? error.message : String(error);
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

function requireStringField(
  task: Record<string, unknown>,
  field: string,
  index: number,
  datasetPath: string
): string {
  const value = task[field];
  if (typeof value !== 'string' || value.length === 0) {
    throw new Error(
      `Benchmark dataset task ${index + 1} field ${field} must be a non-empty string: ${datasetPath}`
    );
  }
  return value;
}

function requireStringArrayField(
  task: Record<string, unknown>,
  field: string,
  index: number,
  datasetPath: string
): string[] {
  const value = task[field];
  if (!Array.isArray(value) || !value.every((item) => typeof item === 'string')) {
    throw new Error(
      `Benchmark dataset task ${index + 1} field ${field} must be an array of strings: ${datasetPath}`
    );
  }
  return value;
}

function optionalStringArrayField(
  task: Record<string, unknown>,
  field: string,
  index: number,
  datasetPath: string
): string[] {
  if (task[field] === undefined) return [];
  return requireStringArrayField(task, field, index, datasetPath);
}

function optionalStringField(task: Record<string, unknown>, field: string): string | undefined {
  const value = task[field];
  return typeof value === 'string' && value.length > 0 ? value : undefined;
}

export function parseBenchmarkDataset(data: unknown, datasetPath: string): BenchmarkDataset {
  if (!isRecord(data) || !Array.isArray(data.tasks)) {
    throw new Error(`Benchmark dataset must contain a tasks array: ${datasetPath}`);
  }
  if (data.tasks.length === 0) {
    throw new Error(`Benchmark dataset has no tasks: ${datasetPath}`);
  }

  return {
    tasks: data.tasks.map((task, index) => {
      if (!isRecord(task)) {
        throw new Error(`Benchmark dataset task ${index + 1} must be an object: ${datasetPath}`);
      }
      return {
        id: requireStringField(task, 'id', index, datasetPath),
        task: requireStringField(task, 'task', index, datasetPath),
        intent: requireStringField(task, 'intent', index, datasetPath),
        relevant_files: requireStringArrayField(task, 'relevant_files', index, datasetPath),
        relevant_types: optionalStringArrayField(task, 'relevant_types', index, datasetPath),
        relevant_keywords: optionalStringArrayField(task, 'relevant_keywords', index, datasetPath),
        irrelevant_files: optionalStringArrayField(task, 'irrelevant_files', index, datasetPath),
        taskSymbolRemoved: optionalStringField(task, 'task_symbol_removed'),
        removedSymbols: optionalStringArrayField(task, 'removed_symbols', index, datasetPath),
      };
    }),
  };
}

export function loadBenchmarkDataset(datasetPath: string): BenchmarkDataset {
  let raw: string;
  try {
    raw = readFileSync(datasetPath, 'utf-8');
  } catch (error) {
    throw new Error(`Unable to read benchmark dataset JSON at ${datasetPath}: ${errorMessage(error)}`);
  }

  try {
    return parseBenchmarkDataset(JSON.parse(raw) as unknown, datasetPath);
  } catch (error) {
    if (error instanceof SyntaxError) {
      throw new Error(`Malformed benchmark dataset JSON at ${datasetPath}: ${error.message}`);
    }
    throw error;
  }
}

/**
 * Load a frozen self-corpus snapshot ({ path, content }[] JSON). Paths are
 * repo-relative strings (e.g. "src/core/scorer.ts") that match dataset.json's
 * relevant_files. Used by the deterministic blocking gate so its reference corpus
 * is stable across repo growth (see benchmarks/freeze-self-corpus.ts).
 */
export function loadCorpusSnapshot(snapshotPath: string): CorpusSnapshotEntry[] {
  let raw: string;
  try {
    raw = readFileSync(snapshotPath, 'utf-8');
  } catch (error) {
    throw new Error(`Unable to read corpus snapshot JSON at ${snapshotPath}: ${errorMessage(error)}`);
  }
  let data: unknown;
  try {
    data = JSON.parse(raw);
  } catch (error) {
    throw new Error(`Malformed corpus snapshot JSON at ${snapshotPath}: ${errorMessage(error)}`);
  }
  const files = isRecord(data) && Array.isArray((data as { files?: unknown }).files)
    ? (data as { files: unknown[] }).files
    : data;
  if (!Array.isArray(files)) {
    throw new Error(`Corpus snapshot must be an array (or { files: [...] }): ${snapshotPath}`);
  }
  return files.map((entry, index) => {
    if (!isRecord(entry) || typeof entry.path !== 'string' || typeof entry.content !== 'string') {
      throw new Error(`Corpus snapshot entry ${index} must be { path: string, content: string }: ${snapshotPath}`);
    }
    return { path: entry.path, content: entry.content };
  });
}

export function parseArgs(argv: string[], benchDir: string): CliOptions {
  const options: CliOptions = {
    dataset: join(benchDir, 'dataset.json'),
    corpus: join(benchDir, '..'),
    strategy: 'all',
    json: false,
    includeTests: false,
    workers: 1,
    maxChunks: null,
    maxFiles: null,
    corpusSnapshot: null,
    grepRounds: GREP_ROUNDS_DEFAULT,
    grepBudget: GREP_BUDGET_DEFAULT,
    symbolRemoved: false,
  };

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === '--dataset') options.dataset = readOptionValue(argv, i++, arg);
    else if (arg === '--corpus') options.corpus = readOptionValue(argv, i++, arg);
    else if (arg === '--corpus-snapshot') options.corpusSnapshot = readOptionValue(argv, i++, arg);
    else if (arg === '--strategy') options.strategy = readOptionValue(argv, i++, arg);
    else if (arg === '--json') options.json = true;
    else if (arg === '--include-tests') options.includeTests = true;
    else if (arg === '--workers') options.workers = parsePositiveInteger(readOptionValue(argv, i++, arg), arg);
    else if (arg === '--max-chunks') options.maxChunks = parsePositiveInteger(readOptionValue(argv, i++, arg), arg);
    else if (arg === '--max-files') options.maxFiles = parsePositiveInteger(readOptionValue(argv, i++, arg), arg);
    else if (arg === '--grep-rounds') options.grepRounds = parsePositiveInteger(readOptionValue(argv, i++, arg), arg);
    else if (arg === '--grep-budget') options.grepBudget = parsePositiveInteger(readOptionValue(argv, i++, arg), arg);
    else if (arg === '--symbol-removed') options.symbolRemoved = true;
    else throw new Error(`Unknown argument: ${arg}`);
  }

  return options;
}

export function resolveStrategies(strategy: string): string[] {
  if (strategy === 'all') return [...ALL_STRATEGIES];
  if (!KNOWN_STRATEGIES.has(strategy)) {
    throw new Error(`Unknown benchmark strategy "${strategy}". Expected one of: all, ${[...KNOWN_STRATEGIES].sort().join(', ')}`);
  }
  return [strategy];
}

function hashString(value: string): number {
  let hash = 2166136261;
  for (let i = 0; i < value.length; i++) {
    hash ^= value.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function seededScore(seed: string): number {
  return hashString(seed) / 0xffffffff;
}

/**
 * Indexed-DB cache for the benchmark corpus (opt-in via SF_BENCH_CACHE_DIR). The
 * ablation pass (--symbol-removed) and any same-config re-run share the IDENTICAL
 * corpus + embeddings — only the query text differs — so re-running the ~0.5s/file
 * ingest (chunk + FTS5 + structural symbols + GPU embedding) is pure waste. With the
 * cache, the first run ingests + persists the SQLite DB; subsequent runs (ablation,
 * re-runs) copy it in and skip ingest entirely. OFF by default (the blocking gate and
 * normal runs are unaffected). Keyed by the ingested file-path set + embedding config
 * (model/device/seed) — deliberately NOT by --symbol-removed (ablation reuses) or
 * retrieval depth (eval-only).
 */
function benchCacheDir(): string | null {
  const dir = process.env.SF_BENCH_CACHE_DIR;
  if (!dir) return null;
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
  return dir;
}

function benchCacheKey(paths: string[]): string {
  const embeddingConfig = [
    process.env.BENCH_EMBEDDING ?? 'deterministic',
    process.env.GPU_EMBEDDING_MODEL ?? 'default',
    process.env.GPU_EMBEDDING_DEVICE ?? '',
    process.env.GPU_EMBEDDING_SEED ?? '',
  ].join('|');
  const pathHash = hashString([...paths].sort().join('\n'));
  const cfgHash = hashString(embeddingConfig);
  return `${pathHash.toString(16)}-${cfgHash.toString(16)}`;
}

function copyDbSet(srcBase: string, dstBase: string): void {
  // Copy the .db plus its WAL/SHM sidecars so a copied DB replays consistently.
  for (const suffix of ['', '-wal', '-shm']) {
    const src = `${srcBase}${suffix}`;
    if (existsSync(src)) copyFileSync(src, `${dstBase}${suffix}`);
  }
}

