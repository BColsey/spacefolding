interface HitDetail {
  path: string;
  rank: number;
}

export interface EvalResult {
  taskId: string;
  task: string;
  intent: string;
  metrics: Metrics;
  details: {
    retrievedPaths: string[];
    relevantPaths: string[];
    hits: string[];
    misses: string[];
    hitDetails: HitDetail[];
    retrievedPathCount: number;
    // Token-cost accounting for the spacefolding-backed strategies (structural/
    // text/vector/hybrid), which return real chunks with token estimates. The JS
    // baselines (bm25/keyword/path-match) return whole-file lists, not chunks, so
    // they carry no chunk-level token cost and this is omitted for them. Used by
    // the chunk-size sweep: smaller chunks should reach the first correct file in
    // fewer tokens (less dead weight per chunk) without losing recall.
    tokenCost?: TokenCost;
  };
}

/**
 * Per-task token-cost of a chunk-returning retrieval. `tokensToFirstHit` is the
 * cumulative token estimate over the ranked chunk list up to and including the
 * first chunk whose file is relevant (null when no relevant file was retrieved) —
 * the tokens-to-first-correct-file metric. `totalTokens` is the sum over all
 * returned chunks at the benchmark retrieval depth.
 */
export interface TokenCost {
  chunksReturned: number;
  totalTokens: number;
  tokensToFirstHit: number | null;
  avgChunkTokens: number;
  // Cumulative token cost up to and including rank i (1-indexed → tokensByRank[i-1]),
  // over the ranked list. Populated by the grep arm so recall@<budget> is computable
  // at ANY token cutoff in post (a gold file at rank r is "within budget B" iff
  // tokensByRank[r-1] <= B) without re-running. Optional: chunk-returning strategies
  // populate tokensToFirstHit directly and may omit the full curve.
  tokensByRank?: number[];
  // Grep dual token model (Phase 8). The headline tokensToFirstHit/tokensByRank above
  // use the MATCHED-CONTEXT model (the agent reads ripgrep's matching lines). The
  // wholeFile* fields hold the WHOLE-FILE model for comparison — the "agent opens each
  // file to confirm" cost, which is real but would make the crossover unfalsifiable if
  // it were the headline (structural returns ~2k-token chunks, so whole-file vs chunk
  // is a unit mismatch structural always wins). Reporting both keeps the chunk-isolation
  // value proposition visible without stacking the deck.
  wholeFileTokensToFirstHit?: number | null;
  wholeFileTokensByRank?: number[];
  wholeFileTotalTokens?: number;
}

