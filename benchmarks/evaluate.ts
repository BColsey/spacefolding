/** Paired-bootstrap contrast (structural − comparator) of a per-task metric. */
export interface PairedContrast {
  comparator: string;
  metric: string;
  mean: number;
  low: number;
  high: number;
}

export interface EvaluationReport {
  dataset: string;
  corpus: string;
  requestedStrategies: string[];
  strategies: StrategySummary[];
  successGate: {
    requiredStrategySummaries: string[];
    missingStrategySummaries: string[];
    // Pre-registered non-inferiority margin (in recall@10 points) for the
    // "structural is not worse than the best lexical arm" half of the gate.
    recallNonInferiorityMargin: number;
    // The strongest lexical baseline (by mean recall@10) structural is measured
    // against for non-inferiority.
    bestLexicalStrategy: string | null;
    // Composite gate contrasts (structural − comparator), paired bootstrap 95% CI.
    recallAt10VsBestLexical: PairedContrast | null;
    hitsAt1VsFts: PairedContrast | null;
    // Half 1: structural recall@10 is non-inferior to the best lexical arm
    // (CI lower bound for structural − bestLexical ≥ −margin).
    recallAt10NonInferior: boolean | null;
    // Half 2: structural strictly beats fts on top-1 localization
    // (CI for structural − fts on hits@1 excludes 0, lower bound > 0).
    hitsAt1BeatsFts: boolean | null;
    // The blocking condition: both halves hold. Present only when computable.
    structuralMeetsGate?: boolean;
  };
  /** Run-level provenance so a report is reproducible and auditable. */
  provenance?: RunProvenance;
}

export interface RunProvenance {
  gitCommit: string;
  gitBranch: string;
  nodeVersion: string;
  platform: string;
  arch: string;
  generatedAt: string;
}

/** Capture run-level provenance for an evaluation report. */
export function buildProvenance(): RunProvenance {
  const git = (args: string[]): string => {
    try {
      const result = spawnSync('git', args, { encoding: 'utf-8' });
      if (result.status === 0) return result.stdout.trim();
    } catch {
      // git unavailable — leave as 'unknown'
    }
    return 'unknown';
  };
  return {
    gitCommit: git(['rev-parse', 'HEAD']),
    gitBranch: git(['rev-parse', '--abbrev-ref', 'HEAD']),
    nodeVersion: process.version,
    platform: process.platform,
    arch: process.arch,
    generatedAt: new Date().toISOString(),
  };
}

export interface CliOptions {
  dataset: string;
  corpus: string;
  strategy: string;
  json: boolean;
  includeTests: boolean;
  workers: number;
  maxChunks: number | null;
  // Cap the ingested corpus to N source files for the scale axis (the Phase-8
  // grep head-to-head runs at 1k/10k/… file sizes). The cap is GOLD-RETAINING:
  // every file named in a task's relevant_files survives regardless of N, and the
  // remainder is filled with the other walked files in deterministic (sorted)
  // order. This keeps recall well-defined at every size — the only thing that
  // varies across cap sizes is the distractor count, isolating the pure
  // corpus-size effect on tokens-to-first-correct-file. null = no cap.
  maxFiles: number | null;
  // Path to a frozen self-corpus snapshot ({path, content}[] JSON). When set, the
  // benchmark ingests these files verbatim instead of walking the live repo tree —
  // so the deterministic blocking gate's reference corpus does NOT drift as the
  // repo grows. See benchmarks/freeze-self-corpus.ts.
  corpusSnapshot: string | null;
  // Agentic-grep baseline (Phase 8 head-to-head) controls. The grep arm runs real
  // ripgrep against the byte-identical indexed corpus, reformulating the query over
  // a few deterministic rounds and walking the ranked files to report
  // tokens-to-first-correct-file + recall@budget. All three are tunable so a skeptic
  // can adjust the baseline and re-run — it must not be a black box.
  grepRounds: number;       // 1..N reformulation rounds (identifier → subtoken → prose)
  grepBudget: number;       // token budget for recall@<budget> (tokensToFirstHit is budget-independent)
  symbolRemoved: boolean;   // re-run every strategy on task_symbol_removed (ablation column)
}

