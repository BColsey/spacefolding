/**
 * Gold-retaining file cap for the scale axis (`--max-files`). Every entry whose
 * relative path is in `goldSet` (the union of all tasks' relevant_files) is kept
 * regardless of `maxFiles`, then the remaining slots are filled with the other
 * entries in their given (sorted, deterministic) order. Because every gold file
 * survives every cap, recall@k is well-defined at every size and the ONLY thing
 * that changes across cap sizes is the distractor count — which is exactly what
 * isolates the pure corpus-size effect the grep crossover depends on. If the gold
 * files alone exceed `maxFiles`, all of them are still kept (recall integrity wins
 * over the nominal cap) and `exceededByGold` is reported.
 */
export function capCorpusByGold<T>(
  entries: T[],
  relPathOf: (entry: T) => string,
  goldSet: Set<string>,
  maxFiles: number
): { kept: T[]; goldKept: number; padding: number; exceededByGold: boolean } {
  const gold: T[] = [];
  const others: T[] = [];
  for (const entry of entries) {
    if (goldSet.has(relPathOf(entry))) gold.push(entry);
    else others.push(entry);
  }
  const padCount = Math.max(0, maxFiles - gold.length);
  const padding = others.slice(0, padCount);
  return {
    kept: [...gold, ...padding],
    goldKept: gold.length,
    padding: padding.length,
    exceededByGold: gold.length > maxFiles,
  };
}

function isMainModule(): boolean {
  return process.argv[1] !== undefined
    && fileURLToPath(import.meta.url) === resolve(process.argv[1]);
}

if (!isMainThread) {
  runBenchmarkWorker().catch((err) => {
    parentPort?.postMessage({
      type: 'error',
      workerId: (workerData as Partial<BenchmarkWorkerPayload> | undefined)?.workerId ?? 0,
      message: errorMessage(err),
    } satisfies BenchmarkWorkerMessage);
    process.exit(1);
  });
} else if (isMainModule()) {
  const benchDir = dirname(fileURLToPath(import.meta.url));
  try {
    const options = parseArgs(process.argv.slice(2), benchDir);
    runEvaluation(options).catch((err) => {
      console.error(`Benchmark failed: ${errorMessage(err)}`);
      process.exit(1);
    });
  } catch (err) {
    console.error(`Benchmark failed: ${errorMessage(err)}`);
    process.exit(1);
  }
}
